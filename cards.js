// СИСТЕМА УНИКАЛЬНЫХ КАРТОЧЕК
let usedCards = {
    romance: { questions: new Set(), actions: new Set() },
    fun: { questions: new Set(), actions: new Set() },
    adult: { questions: new Set(), actions: new Set() },
    dates: { questions: new Set(), actions: new Set(), places: new Set() }
};

// Функция для получения уникальной карточки
function getUniqueCard(theme, type) {
    const cards = CardsBank[theme][type];
    
    // Проверяем, есть ли неиспользованные карточки
    const unusedCards = cards.filter(card => !usedCards[theme][type].has(card));
    
    if (unusedCards.length === 0) {
        // Все карточки использованы - сбрасываем и перемешиваем
        usedCards[theme][type] = new Set();
        console.log(`Все карточки ${theme}/${type} использованы, начинаем заново`);
        return getUniqueCard(theme, type);
    }
    
    // Выбираем случайную карточку из неиспользованных
    const card = unusedCards[Math.floor(Math.random() * unusedCards.length)];
    
    // Добавляем в использованные
    usedCards[theme][type].add(card);
    
    return card;
}

// Функция для получения уникального места
function getUniquePlace() {
    const unusedPlaces = InterestingPlaces.filter(place => !usedCards.dates.places.has(place));
    
    if (unusedPlaces.length === 0) {
        // Все места использованы - сбрасываем
        usedCards.dates.places = new Set();
        console.log('Все места использованы, начинаем заново');
        return getUniquePlace();
    }
    
    const place = unusedPlaces[Math.floor(Math.random() * unusedPlaces.length)];
    usedCards.dates.places.add(place);
    
    return place;
}

// Функция для получения случайной карточки
function getRandomCard(theme, type = null) {
    if (!CardsBank[theme]) {
        theme = 'romance'; // fallback
    }
    
    // Если тип не указан, выбираем случайно
    if (!type) {
        type = Math.random() > 0.5 ? 'questions' : 'actions';
    }
    
    // Если тема "свидания" и выпало место (20% шанс)
    if (theme === 'dates' && Math.random() < 0.2 && InterestingPlaces.length > 0) {
        const place = getUniquePlace();
        // ОСНОВНОЕ ИСПРАВЛЕНИЕ: Возвращаем правильную структуру для мест
        return {
            ...place,
            text: `${place.name} (${place.city}) - ${place.description}`,
            isPlace: true
        };
    }
    
    // Для обычных карточек
    return {
        text: getUniqueCard(theme, type),
        isPlace: false
    };
}

// Функция для сброса статистики
function resetCardUsage() {
    usedCards = {
        romance: { questions: new Set(), actions: new Set() },
        fun: { questions: new Set(), actions: new Set() },
        adult: { questions: new Set(), actions: new Set() },
        dates: { questions: new Set(), actions: new Set(), places: new Set() }
    };
    console.log('Статистика карточек сброшена');
}

// Получить количество оставшихся карточек
function getRemainingCards(theme) {
    let remaining = 0;
    
    if (theme === 'dates') {
        remaining += CardsBank.dates.questions.length - usedCards.dates.questions.size;
        remaining += CardsBank.dates.actions.length - usedCards.dates.actions.size;
        remaining += InterestingPlaces.length - usedCards.dates.places.size;
    } else {
        remaining += CardsBank[theme].questions.length - usedCards[theme].questions.size;
        remaining += CardsBank[theme].actions.length - usedCards[theme].actions.size;
    }
    
    return remaining;
}

// Функция для замены имён в тексте
function replaceNames(text, name1, name2) {
    if (!text || !name1) return text;
    
    let result = text.replace(/\[Имя1\]/g, name1);
    result = result.replace(/\[Имя2\]/g, name2 || 'партнёр');
    
    return result;
}

