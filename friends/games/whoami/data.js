    // –°–æ—Ö—Ä–∞–Ω–∏—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∏–≥—Ä—ã
    saveGameStats(stats) {
        const currentStats = this.loadGameStats();
        
        currentStats.gamesPlayed++;
        currentStats.totalScore += stats.score || 0;
        currentStats.wordsGuessed += stats.wordsGuessed || 0;
        currentStats.totalTime += stats.time || 0;
        currentStats.totalRounds += stats.rounds || 0;
        
        // –û–±–Ω–æ–≤–ª—è–µ–º –ª—É—á—à–∏–π —Å—á–µ—Ç
        if (stats.score > currentStats.bestScore) {
            currentStats.bestScore = stats.score;
        }
        
        // –û–±–Ω–æ–≤–ª—è–µ–º —Ä–µ–∫–æ—Ä–¥ —Å–∫–æ—Ä–æ—Å—Ç–∏
        if (stats.bestTime && (!currentStats.bestTime || stats.bestTime < currentStats.bestTime)) {
            currentStats.bestTime = stats.bestTime;
        }
        
        // –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
        if (stats.categories) {
            stats.categories.forEach(category => {
                if (!currentStats.categoryStats[category.id]) {
                    currentStats.categoryStats[category.id] = { used: 0, guessed: 0 };
                }
                currentStats.categoryStats[category.id].used += category.used || 0;
                currentStats.categoryStats[category.id].guessed += category.guessed || 0;
            });
        }
        
        // –î–æ–±–∞–≤–ª—è–µ–º –≤ –∏—Å—Ç–æ—Ä–∏—é
        currentStats.recentGames.unshift({
            id: Date.now(),
            date: new Date().toISOString(),
            score: stats.score || 0,
            players: stats.players || 0,
            time: stats.time || 0,
            mode: stats.mode || 'classic',
            categories: stats.categories || []
        });
        
        // –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –∏—Å—Ç–æ—Ä–∏—é 50 –∏–≥—Ä–∞–º–∏
        if (currentStats.recentGames.length > 50) {
            currentStats.recentGames = currentStats.recentGames.slice(0, 50);
        }
        
        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–≥—Ä–æ–∫–æ–≤ —Å –∏—Ö —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–æ–π
        if (stats.playersData) {
            stats.playersData.forEach(playerData => {
                const existingPlayer = currentStats.playerStats.find(p => p.name === playerData.name);
                if (existingPlayer) {
                    existingPlayer.games++;
                    existingPlayer.totalScore += playerData.score;
                    existingPlayer.wordsGuessed += playerData.wordsGuessed;
                    if (playerData.score > existingPlayer.bestScore) {
                        existingPlayer.bestScore = playerData.score;
                    }
                } else {
                    currentStats.playerStats.push({
                        name: playerData.name,
                        games: 1,
                        totalScore: playerData.score,
                        bestScore: playerData.score,
                        wordsGuessed: playerData.wordsGuessed
                    });
                }
            });
        }
        
        try {
            localStorage.setItem('whoami_stats', JSON.stringify(currentStats));
            return true;
        } catch (error) {
            console.error('–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏:', error);
            return false;
        }
    },

