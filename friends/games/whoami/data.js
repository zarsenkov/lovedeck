    getAllWords(categoryId = null) {
        const allWords = [];
        
        // –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–µ —Å–ª–æ–≤–∞
        this.categories.forEach(category => {
            if (!categoryId || category.id === categoryId) {
                category.words.forEach(word => {
                    allWords.push({
                        ...word,
                        category: category.name,
                        categoryId: category.id,
                        isCustom: false
                    });
                });
            }
        });
        
        // –î–æ–±–∞–≤–ª—è–µ–º –∫–∞—Å—Ç–æ–º–Ω—ã–µ —Å–ª–æ–≤–∞
        const customWords = this.loadCustomWords();
        customWords.forEach(word => {
            if (!categoryId || word.categoryId === categoryId) {
                allWords.push(word);
            }
        });
        
        return allWords;
    },

    // –ü–æ–∏—Å–∫ —Å–ª–æ–≤
    searchWords(query, categoryId = null) {
        const words = this.getAllWords(categoryId);
        const searchQuery = query.toLowerCase().trim();
        
        if (!searchQuery) return words;
        
        return words.filter(word => {
            return word.word.toLowerCase().includes(searchQuery) ||
                   word.tags?.some(tag => tag.toLowerCase().includes(searchQuery)) ||
                   word.hint?.toLowerCase().includes(searchQuery);
        });
    },

    // –ü–æ–ª—É—á–∏—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    getCategoryStats() {
        const stats = this.loadGameStats();
        const categoryStats = [];
        
        this.categories.forEach(category => {
            const catStat = stats.categoryStats[category.id] || { used: 0, guessed: 0 };
            const percentage = catStat.used > 0 ? Math.round((catStat.guessed / catStat.used) * 100) : 0;
            
            categoryStats.push({
                ...category,
                used: catStat.used,
                guessed: catStat.guessed,
                percentage: percentage,
                accuracy: catStat.used > 0 ? (catStat.guessed / catStat.used) : 0
            });
        });
        
        return categoryStats.sort((a, b) => b.used - a.used);
    },

