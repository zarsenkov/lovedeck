// Функция для получения случайных слов
function getRandomWords(category, count) {
    const words = [...aliasWords[category]];
    const result = [];
    
    // Перемешиваем массив
    for (let i = words.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [words[i], words[j]] = [words[j], words[i]];
    }
    
    // Берем нужное количество
    for (let i = 0; i < count && i < words.length; i++) {
        result.push(words[i]);
    }
    
    return result;
}
