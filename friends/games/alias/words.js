// –ü–æ–ª—É—á–µ–Ω–∏–µ —Å–ª—É—á–∞–π–Ω—ã—Ö —Å–ª–æ–≤ –∏–∑ –∫–∞—Ç–µ–≥–æ—Ä–∏–π
function getRandomWords(categories, count) {
    // –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ —Å–ª–æ–≤–∞ –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π
    let allWords = [];
    
    if (categories.includes('all')) {
        // –ï—Å–ª–∏ –≤—ã–±—Ä–∞–Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—è "–≤—Å–µ", –±–µ—Ä–µ–º –≤—Å–µ —Å–ª–æ–≤–∞
        allWords = [...aliasWords.all.words];
    } else {
        // –ò–Ω–∞—á–µ —Å–æ–±–∏—Ä–∞–µ–º —Å–ª–æ–≤–∞ —Ç–æ–ª—å–∫–æ –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π
        categories.forEach(category => {
            if (aliasWords[category] && aliasWords[category].words) {
                allWords.push(...aliasWords[category].words);
            }
        });
    }
    
    // –£–±–∏—Ä–∞–µ–º –¥—É–±–ª–∏–∫–∞—Ç—ã
    allWords = [...new Set(allWords)];
    
    // –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –º–∞—Å—Å–∏–≤
    for (let i = allWords.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [allWords[i], allWords[j]] = [allWords[j], allWords[i]];
    }
    
    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω—É–∂–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–ª–æ–≤
    return allWords.slice(0, count);
}

